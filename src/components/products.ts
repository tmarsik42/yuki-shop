export function getCategory(chosenCategories) {
	if (Array.isArray(chosenCategories)) {
		const tmp = [];
		chosenCategories.forEach((v) => tmp.push(findCategory(v)));
		return tmp;
	} else {
		return findCategory(chosenCategories);
	}

	function findCategory(c) {
		if (categories.has(c)) {
			return categories.get(c);
		} else {
			console.log('Category does not exist.');
		}
	}
}

